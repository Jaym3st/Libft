#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*i;

	i = (char *)s;
	if (c > 255)
		c %= 256;
	while (*i != c)
	{
		if (*i == '\0')
		{
			return (NULL);
		}
		i++;
	}
	return (i);
}
